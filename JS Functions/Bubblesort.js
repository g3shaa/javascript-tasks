// Функция за сортиране на масив по алгоритъм Bubble sort
function bubbleSort(arr) {
    var len = arr.length; // определяме дължината на масива
    // вложени цикли за сравняване на всички двойки елементи в масива
    for (var i = 0; i < len; i++) { // първи цикъл за преминаване през всички елементи
        for (var j = 0; j < len - 1; j++) { // втори цикъл за сравняване на двойки елементи
            if (arr[j] > arr[j + 1]) { // проверка дали текущият елемент е по-голям от следващия
                var temp = arr[j]; // временна променлива за размяна на стойности на елементите
                arr[j] = arr[j + 1]; // размяна на елементите
                arr[j + 1] = temp; // размяна на елементите
            }
        }
    }
    return arr; // връща сортирания масив
}

// Тестване на Bubble sort
var arr = [64, 34, 25, 12, 22, 11, 90];
console.log("Масивът преди сортирането: " + arr);
arr = bubbleSort(arr);
console.log("Масивът след сортирането: " + arr);